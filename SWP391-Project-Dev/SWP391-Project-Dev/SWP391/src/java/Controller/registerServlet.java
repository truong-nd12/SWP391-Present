    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        String url = "RegisterPage.jsp";
        HttpSession session = request.getSession();
        String username = request.getParameter("username");
        String name = request.getParameter("name");
        String email = request.getParameter("email");
        String password = request.getParameter("password");
        String rpass = request.getParameter("resetPassword");
        String phone = request.getParameter("phone");
        CustomerDAO cus = new CustomerDAO();
        AdminDAO admin = new AdminDAO();
        Customer customer = cus.getCusEmail(email);
        Customer customer_1 = cus.getCusName(username);
        if (customer != null) {
            request.setAttribute("rerr", "this email have been used");
        } else {
            if (customer_1 != null || admin.getAccByName(username) != null) {
                request.setAttribute("rerr", "this name have been used");
            } else if (!password.equals(rpass)) {
                request.setAttribute("rerr", "Password and Re-password not match ");
            } else {
                customer = cus.addCustomer(username, email, phone, MD5.getMd5(password),"");
                session.setAttribute("acc", customer);
                url = "HomePage";
            }
        }
        request.getRequestDispatcher(url).forward(request, response);
    }

